      <dt id="sp-salut">
        Salut
      </dt>
      <dd>
        L’auteur de l’acte y exprime au destinataire son salut (lat. :
        <i>salutatio</i>, <i>modus salutandi</i>). Celui-ci est de règle
        quand il y a une adresse.<br>
        VID, n° 194:
        <a
          href="https://www.cei.lmu.de/VID/#194"
        >https://www.cei.lmu.de/VID/#194</a><br>
        Le salut peut être complété :
        <ul>
          <li>d’une formule de souhait</li>
        </ul>
      </dd>

      <dt id="sp-perpet">
        Formule de perpétuité
      </dt>
      <dd>
        La formule de perpétuité peut venir remplacer le salut. Elle
        déclare que les droits créés par le document ne sont pas
        circonscrits dans le temps.<br>
        VID n° 195:
        <a
          href="https://www.cei.lmu.de/VID/#195"
        >https://www.cei.lmu.de/VID/#195</a><br>
        Exemple : « <i>In perpetuum</i> »
      </dd>

      <dt id="sp-preambule">
        Préambule
      </dt>
      <dd>
        Le préambule (lat. : <i>exordium</i>, <i>arenga</i>,
        <i>proemium</i>) est l’ensemble de considérations générales, à
        valeur universelle, détachées du contexte particulier, qui
        motivent et légitiment l’acte.<br>
        VID n° 196bis :
        <a
          href="https://www.cei.lmu.de/VID/#196bis"
        >https://www.cei.lmu.de/VID/#196bis</a><br>
        Il peut s’agir :
        <ul>
          <li>d’un préambule court</li>
        </ul>
      </dd>

      <dt id="sp-notification">
        Notification
      </dt>
      <dd>
        La notification (lat. : <i>notificatio</i>, <i>publicatio</i>,
        <i>promulgatio</i>) est une formule-outil qui déclare la volonté
        de porter à la connaissance des lecteurs ou auditeurs de
        l’acte.<br>
        VID n° 196:
        <a
          href="https://www.cei.lmu.de/VID/#196"
        >https://www.cei.lmu.de/VID/#196</a>
        La notification peut-être :
        <ul>
          <li>
            Universelle<br>
            Exemple : «
            <i>Savoir faisons à tous présents et à venir que</i> »
          </li>
          <li>
            Collective<br>
            Exemple : «
            <i>Savoir faisons aux prélats de ladite ville</i> »
          </li>
          <li>Personnelle</li>
        </ul>
      </dd>

