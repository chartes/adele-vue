      <dt id="tra-orig-mul">
        Original multiple
      </dt>
      <dd>
        Exemplaires d’un même acte dressés simultanément, pour être
        remis aux diverses parties intéressées ou pour en assurer la
        conservation. <br>
        VID n° 43 [à l’exception des chirographes] :
        <a
          href="https://www.cei.lmu.de/VID/#43"
        >https://www.cei.lmu.de/VID/#43</a>
