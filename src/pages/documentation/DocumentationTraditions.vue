      <dt id="tra-chiro">
        Chirographe
      </dt>
      <dd>
        Acte établi en deux ou plusieurs exemplaires, sur une même
        feuille de parchemin, coupée par le milieu, pour que chacun des
        contractants ait un original de la pièce.<br>
        VID n° 44 :
        <a
          href="https://www.cei.lmu.de/VID/#44"
        >https://www.cei.lmu.de/VID/#44</a>
      </dd>

