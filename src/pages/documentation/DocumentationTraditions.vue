    <p>
      La tradition d’un acte a pu être définie comme la chaîne des états
      successifs de l’acte, depuis le moment de sa création jusqu’à
      l’état que nous avons sous les yeux. Ces états sont, sans
      exhaustivité, la copie intégrale ou partielle, l’extrait, la
      simple mention, ou encore la falsification, la reproduction
      dessinée ou photographiée, la traduction. D’abord conçue sous
      l’empire de la philologie comme méthode permettant de débusquer
      les faiblesses des copies et d’appuyer la reconstitution du « bon
      » texte, l’étude de la tradition a inscrit à son programme
      l’analyse de l’attention portée à l’acte au fil des siècles, de
      ses lectures et, en un mot, de sa réception.
    </p>
