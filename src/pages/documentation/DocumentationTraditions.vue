        Pancarte
      </dt>
      <dd>
        Acte destiné à confirmer des droits et des biens dont les titres
        ont été perdus. Se dit par extension d’un cartulaire contenant
        les titres d’un établissement ecclésiastique.<br>
        VID n° 50 :
