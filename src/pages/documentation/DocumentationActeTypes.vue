      éminemment diplomatique. Les types diplomatique précis, élaborés
      surtout au sein des chancelleries souveraines, sont pour leur part
      explicités dans la page de titre du dossier. Les documents de
      gestion sont organisés selon leur fonction et/ou leur type de
      producteur.
